# HostMasters — Fluxo Financeiro e Planos

## Planos e taxas

| Plano   | Comissão | Mensalidade | Taxa limpeza                        | Diferencial                          |
|---------|----------|-------------|-------------------------------------|--------------------------------------|
| STARTER | 22%      | €0          | €70/saída (sempre)                  | Base — sem manutenção incluída       |
| BASIC   | 20%      | €89/mês     | €60/saída (sempre)                  | Manutenção preventiva, relatório multilingue |
| MID     | 17%      | €159/mês    | €45 se <5 noites / incluída ≥5 noites | AI pricing + Smart Lock            |
| PREMIUM | 13%      | €269/mês    | €35 se <3 noites / incluída ≥3 noites | Fiscal (Modelo 179 + IRNR), upsells |

## Fluxo de um pagamento

```
Hóspede paga Airbnb/Booking
        ↓
Plataforma deposita no banco (valor bruto)
        ↓
Admin cria Payout
  ├── grossAmount = valor recebido da plataforma
  ├── commission  = gross × taxa do plano do owner
  └── netAmount   = gross − commission
        ↓
Admin clica "Mark Paid"
  ├── paidAt = agora
  ├── Invoice criado automaticamente (isAutoGenerated=true)
  ├── Email "Owner Statement" enviado ao proprietário
  └── Email "Payment confirmed" de confirmação
        ↓
Dashboard actualiza (auto-refresh 60s)
```

## Datas de payout por plataforma

| Plataforma | Regra |
|-----------|-------|
| Airbnb | Checkout + 1 dia + 2 dias úteis |
| Booking.com | Último dia do mês do checkout |
| Direct / Other | Checkout + 7 dias |

## O que conta como receita HostMasters

✅ Comissões de arrendamentos (payout.commission WHERE PAID)
✅ Invoices manuais (isAutoGenerated=false, status=PAID)

❌ payout.grossAmount (dinheiro do owner, só passa pela plataforma)
❌ Invoices isAutoGenerated=true (registos contabilísticos, não receita HM)

## Relatório mensal automático

Cron job activo: dia 1 de cada mês, 08:00 UTC
- Calcula financeiros do mês anterior por propriedade
- Envia email com breakdown ao owner
- Guarda MonthlyReport.sentAt para evitar duplicados
FIN_EOF
