# HostMasters — Stack Técnico

## Tecnologias principais

| Camada | Tecnologia |
|--------|-----------|
| Framework | Next.js 14 App Router + TypeScript |
| Base de dados | PostgreSQL (Neon) via Prisma ORM |
| Auth | NextAuth.js com JWT (roles na sessão) |
| Email | Resend (transacional) |
| Pagamentos | Stripe (subscrições + invoices) |
| Hosting | Vercel (deploy automático a cada push para main) |
| Testes | Vitest (27 unit tests em src/__tests__/) |
| PDF | jsPDF + jspdf-autotable |

## Deploy

```
git push origin main
  → Vercel detecta
  → prisma db push (sync schema)
  → next build
  → deploy automático
```

## Variáveis de ambiente necessárias

- DATABASE_URL — Neon PostgreSQL connection string
- NEXTAUTH_SECRET — segredo para JWT
- RESEND_API_KEY — API key do Resend
- STRIPE_SECRET_KEY — Stripe payments
- CRON_SECRET — protege o endpoint de cron mensal
- NEXT_PUBLIC_APP_URL — URL da aplicação (para links em emails)

## Cron jobs activos

| Job | Schedule | Função |
|-----|----------|--------|
| /api/cron/monthly-statements | 0 8 1 * * | Envia relatórios mensais a todos os owners |

## Regras críticas (nunca violar)

1. **Moeda**: base EUR. `useCurrency()` é só display — nunca em inputs de valor real
2. **subscriptionPlan**: não está na sessão — sempre ler da DB
3. **Comissões**: nunca hardcodar — usar `PLAN_COMMISSION` de `src/lib/finance.ts`
4. **Invoices auto-gerados**: `isAutoGenerated=true` — não contam como receita HM
5. **Migrations**: não usar `db push` em produção (usar `migrate deploy`) — EXCEPTO neste projecto que usa `db push` via Vercel

## Roadmap técnico

### Q2 2026 (em curso)
- PriceLabs API — pricing dinâmico para MID/PREMIUM
- Colecção de dados próprios via PricingDataPoint

### Q3–Q4 2026
- Nordigen/GoCardless — Open Banking (detecção automática de pagamentos)
- Stripe webhooks — subscrições automáticas
- Meta Ads API — sync de spend e CAC
- Brevo — nurturing de leads

### Longo prazo
- Modelo AI próprio treinado nos dados da plataforma
