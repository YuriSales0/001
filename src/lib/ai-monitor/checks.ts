/**
 * AI Monitor — check definitions
 *
 * Each check returns a { checkType, severity, message, count, details? }
 * when an anomaly is detected, or null when everything is fine.
 *
 * Severity:
 *   CRITICAL — imediata acção, afecta dinheiro ou segurança
 *   HIGH     — requer acção do admin em <24h
 *   MEDIUM   — operacional, não-bloqueante
 *   LOW      — informativo, nice-to-have
 */
import { prisma } from '@/lib/prisma'
import { PLAN_COMMISSION } from '@/lib/finance'

export type CheckSeverity = 'CRITICAL' | 'HIGH' | 'MEDIUM' | 'LOW'

export type CheckResult = {
  checkType: string
  severity: CheckSeverity
  message: string
  count: number
  // eslint-disable-next-line @typescript-eslint/no-explicit-any
  details?: Record<string, any>
  canAutoFix?: boolean
}

export type CheckFn = (now: Date) => Promise<CheckResult | null>

