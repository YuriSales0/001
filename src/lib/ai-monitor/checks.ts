const p1_paidPayoutsNoInvoice: CheckFn = async () => {
  const paidPayoutsCount = await prisma.payout.count({ where: { status: 'PAID' } })
  const autoInvoicesCount = await prisma.invoice.count({ where: { isAutoGenerated: true, status: 'PAID' } })
  const missing = Math.max(0, paidPayoutsCount - autoInvoicesCount)
  if (missing === 0) return null
  return {
    checkType: 'PAYOUT_NO_INVOICE',
    severity: 'HIGH',
    message: `${missing} payout(s) pago(s) sem invoice auto-gerado`,
    count: missing,
  }
}

const p1b_duplicateInvoices: CheckFn = async () => {
  const paidPayoutsCount = await prisma.payout.count({ where: { status: 'PAID' } })
  const autoInvoicesCount = await prisma.invoice.count({ where: { isAutoGenerated: true, status: 'PAID' } })
  const duplicates = Math.max(0, autoInvoicesCount - paidPayoutsCount)
  if (duplicates === 0) return null
  return {
    checkType: 'INVOICE_DUPLICATE',
    severity: 'CRITICAL',
    message: `${duplicates} invoice(s) auto-gerado(s) a mais que payouts pagos — possível duplicação`,
    count: duplicates,
  }
}

